'''
função print
- (,) printa outra coisa já separado
- entre prints tem quebra de linha
- sep => separador, padrao é o espaço normal
- sem simbolo de atribuição, é um argumento não nomeado
- end => adiciona depois de um ultimo argumento algo pro ptint, -> padrão end = quebra de linha
'''

print(123, 456, "10 11", sep = '..', end = "\n")
print(321, 654, sep = " and ", end = "##")
print(1337, 491, sep="--", end = "e\n")
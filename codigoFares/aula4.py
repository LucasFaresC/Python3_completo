'''
Tipo int e float
-  int -> numero inteiro
- float -> numero real | numero com ponto flutuante | numero com virgula
- Função => tudo que é minusculo e é executavel (callabe) se chama de função 
- Classe => geralmente começa com minusculo | type é classe -> retorna o tipo do argumento
'''

print(11) #int numero inteiro
print(-11)# tbm numero inteiro
print(1.1) #float é numero com virgula, mas se escreve com ponto => padrão américano

print(type(111)) #classe type retorna o tipo da variavel
# <class 'int'>
print(type("isso é um texto em portugues"))
# <class 'str'>

print(type(0.0))
print(type(0))
